package modelsSecuential;

/**
 * Esta clase representa el ingreso de un compilador para el programa
 * @author herna
 *
 */
public class Compiler {
	private String name;

	public Compiler() {
		
	}

	public Compiler(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
